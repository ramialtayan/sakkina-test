function applyLang(lang) {
  const dict = translations[lang];
  const body = document.body;
  body.classList.remove("ltr", "rtl");
  body.classList.add(lang === "ar" ? "rtl" : "ltr");

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) el.textContent = dict[key];
  });

  const heightPlaceholder = document.querySelector(
    'select[name="height"] option[value=""]'
  );
  if (heightPlaceholder && dict.fieldHeightPlaceholder) {
    heightPlaceholder.textContent = dict.fieldHeightPlaceholder;
  }

  const thanks = document.getElementById("thanks-msg");
  if (thanks && dict.formThanks) {
    thanks.setAttribute("data-text", dict.formThanks);
  }

  const btnEn = document.getElementById("btn-en");
  const btnAr = document.getElementById("btn-ar");
  btnEn.classList.toggle("active", lang === "en");
  btnAr.classList.toggle("active", lang === "ar");
}

document.addEventListener("DOMContentLoaded", () => {
  applyLang("en");

  document.getElementById("btn-en").addEventListener("click", () => {
    applyLang("en");
  });
  document.getElementById("btn-ar").addEventListener("click", () => {
    applyLang("ar");
  });

  const form = document.getElementById("order-form");
  const thanks = document.getElementById("thanks-msg");

  form.addEventListener("submit", (e) => {
    e.preventDefault();
    if (thanks) {
      const currentLang = document.body.classList.contains("rtl") ? "ar" : "en";
      thanks.textContent = translations[currentLang].formThanks;
      thanks.style.display = "block";
    }
    form.reset();
  });
});
